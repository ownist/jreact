// short conditionals
let idol = "Tourist";

// instead of doing this
if (idol === "Tourist") {
  console.log("salute");
}

// we can use && operator
idol === "Tourist" && console.log("love emoji");

// and instead of doing this
if (idol !== "Tourist") {
  console.log("angry emoji");
}

// we can use ||
idol === "Tourist" || console.log("angry emoji");

// convert any value to boolean

/**
 * kono value ke boolean e convert korar jonno sei value er age duita !! sign bosiye dilei seita boolean value hoye jai
 */
console.log(!!"shahed"); // true
console.log(!!1); // true
console.log(!!0); // false
console.log(!!undefined); // false

// Boolean function use korew eita shortcut way te kora jay
